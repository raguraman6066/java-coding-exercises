public class StringInt {
    public static void main(String[] args) {
        String k="10";
        String m="6";
        int i=Integer.parseInt(k);
        int j=Integer.parseInt(m);
        System.out.println(i+j);

        int l=100;
        String o=String.valueOf(l);
        System.out.println(o+"23");
    }
}
